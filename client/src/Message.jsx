function hasImageExtension(value) {
  let parser = document.createElement('a')
  parser.href = value;
  return (/\.(gif|jpg|jpeg|tiff|png)$/i).test(parser.pathname);
}

function isImageUrl(value) {
  return validUrl(value) && hasImageExtension(value);
}

class Message extends Component {
  render() {
    const username = this.props.username || "Anonymous";
    const divStyle = {
      color: this.props.color
    }
    let content = this.props.content;
    if(isImageUrl(content)) {
      content = <img src={content}/>;
    }
    return (
        <div className={getClassForMessageType(this.props.type)} style={divStyle}>
        <span className="username">{username}</span>
        <span className="content">{content}</span>
      </div>
    );
  }
}
export default Message;
